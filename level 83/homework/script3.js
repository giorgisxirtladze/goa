let cities = ["Tbilisi", "Batumi", "Kutaisi"];
let removedCity = cities.shift();
console.log("Removed city:", removedCity);
console.log("Array after shift:", cities);

let numbers = [10, 20, 30, 40];
let firstRemoved = numbers.shift();
let secondRemoved = numbers.shift();
console.log("Removed numbers:", firstRemoved, secondRemoved);
console.log("Array after two shifts:", numbers);
